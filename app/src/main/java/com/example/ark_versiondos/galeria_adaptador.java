package com.example.ark_versiondos;

public class galeria_adaptador {
    int imagen;
    String animal;

    public galeria_adaptador(int imagen) {
        this.imagen = imagen;
        this.animal = animal;
    }

    public int getImagen() {
        return imagen;
    }

    public void setImagen(int imagen) {
        this.imagen = imagen;
    }

    public String getAnimal() {
        return animal;
    }

    public void setAnimal(String animal) {
        this.animal = animal;
    }
}
